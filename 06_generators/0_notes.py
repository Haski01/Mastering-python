#                       [What are Generators?]

'''
 - Generators are a type of iterable (like lists, tuples, etc.), but they generate values on the fly instead of storing them in memory.

 - They are memory efficient because they yield values one at a time using the yield keyword.

 - They are useful for working with large datasets, infinite sequences, or streams of data
'''

# [Why Generators? (Advantages)]

'''
 - Memory Efficient → Do not store entire data in memory.
 - Lazy Evaluation → Values generated only when needed.
 - Infinite Sequences → Can model endless streams of data.
'''

